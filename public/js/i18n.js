// Current language
let _currentLang = localStorage.getItem('app-lang') || 'es';

function t(key, params) {
  const str = (I18N[_currentLang] && I18N[_currentLang][key]) || (I18N.es[key]) || key;
  if (!params) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => params[k] !== undefined ? params[k] : `{${k}}`);
}

function getLang() {
  return _currentLang;
}

function setLang(lang) {
  if (!I18N[lang]) return;
  _currentLang = lang;
  localStorage.setItem('app-lang', lang);
  translateStaticElements();
  // Re-render current page
  if (window._currentPage && window.Pages[window._currentPage]) {
    updatePageHeader();
    window.Pages[window._currentPage]();
  }
  updateNavLabels();
  updateLoginTexts();
  // Update sidebar flag
  const flag = document.querySelector('.lang-current-flag');
  if (flag) flag.innerHTML = FLAG_SVG[_currentLang];
}

function getDateLocale() {
  const map = { es: 'es-AR', en: 'en-US', pt: 'pt-BR' };
  return map[_currentLang] || 'es-AR';
}

function getMonthNames() {
  return [
    t('month_jan'), t('month_feb'), t('month_mar'), t('month_apr'),
    t('month_may'), t('month_jun'), t('month_jul'), t('month_aug'),
    t('month_sep'), t('month_oct'), t('month_nov'), t('month_dec')
  ];
}

function translateStaticElements() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPh);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    el.setAttribute('aria-label', t(el.dataset.i18nAria));
  });
}

function updateNavLabels() {
  document.querySelectorAll('.nav-links a[data-page]').forEach(a => {
    const page = a.dataset.page;
    const key = `nav_${page}`;
    const span = a.querySelector('span');
    if (span) span.textContent = t(key);
  });
}

function updatePageHeader() {
  const page = window._currentPage;
  if (!page) return;
  const titleEl = document.getElementById('page-title');
  const subEl = document.getElementById('page-subtitle');
  if (titleEl) titleEl.textContent = t(`page_${page}`);
  if (subEl) subEl.textContent = t(`page_${page}_sub`);
}

function renderLoginLangSwitcher() {
  const container = document.getElementById('login-lang-switcher');
  if (!container) return;

  const langs = ['es', 'en', 'pt'];
  container.innerHTML = langs.map(lang => `
    <button class="login-lang-btn ${lang === _currentLang ? 'active' : ''}" data-lang="${lang}">
      <div class="login-lang-flag">${FLAG_SVG[lang]}</div>
    </button>
  `).join('');

  container.querySelectorAll('.login-lang-btn').forEach(btn => {
    btn.onclick = () => setLang(btn.dataset.lang);
  });
}

