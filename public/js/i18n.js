function renderLangSelector() {
  const existing = document.getElementById('lang-selector');
  if (existing) existing.remove();

  const container = document.createElement('div');
  container.id = 'lang-selector';
  container.className = 'lang-selector';

  const langs = ['es', 'en', 'pt'];

  container.innerHTML = `
    <button class="lang-toggle" id="lang-toggle" title="${t('lang_label')}">
      <div class="lang-current-flag">${FLAG_SVG[_currentLang]}</div>
    </button>
  `;

  const toggle = container.querySelector('#lang-toggle');

  toggle.addEventListener('click', (e) => {
    e.stopPropagation();
    // Remove any existing dropdown
    const old = document.getElementById('lang-floating-dropdown');
    if (old) { old.remove(); return; }

    // Create floating dropdown
    const dropdown = document.createElement('div');
    dropdown.id = 'lang-floating-dropdown';
    dropdown.className = 'lang-floating-dropdown';

    dropdown.innerHTML = langs.map(lang => `
      <button class="lang-option ${lang === _currentLang ? 'active' : ''}" data-lang="${lang}">
        <div class="lang-flag">${FLAG_SVG[lang]}</div>
        <span class="lang-code">${lang.toUpperCase()}</span>
        ${lang === _currentLang ? '<svg class="lang-check" width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="3"><path d="M20 6L9 17l-5-5"/></svg>' : ''}
      </button>
    `).join('');

    document.body.appendChild(dropdown);

    // Position it next to the toggle
    const rect = toggle.getBoundingClientRect();
    dropdown.style.top = (rect.top + rect.height / 2 - dropdown.offsetHeight / 2) + 'px';
    dropdown.style.left = (rect.right + 10) + 'px';

    // Animate in
    requestAnimationFrame(() => dropdown.classList.add('open'));

    // Click handlers
    dropdown.querySelectorAll('.lang-option').forEach(btn => {
      btn.addEventListener('click', (ev) => {
        ev.stopPropagation();
        dropdown.remove();
        setLang(btn.dataset.lang);
      });
    });

    // Close on outside click
    setTimeout(() => {
      document.addEventListener('click', function closeDD() {
        const dd = document.getElementById('lang-floating-dropdown');
        if (dd) dd.remove();
        document.removeEventListener('click', closeDD);
      }, { once: true });
    }, 10);
  });

  return container;
}
